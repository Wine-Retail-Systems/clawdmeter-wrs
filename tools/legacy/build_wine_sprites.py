# ---------- Builder ----------

def parse_grid(s):
    lines = [ln for ln in s.split("\n") if ln.strip("\r ") != ""]
    # Tolerate one missing blank line at top/bottom but enforce 20 content rows.
    if len(lines) != 20:
        raise ValueError(f"grid must have 20 rows, got {len(lines)}")
    grid = []
    for r, line in enumerate(lines):
        if len(line) != 20:
            raise ValueError(f"row {r} length {len(line)} != 20: {line!r}")
        row = []
        for c, ch in enumerate(line):
            if ch == ".":
                row.append(0)
            elif "0" <= ch <= "9":
                row.append(int(ch))
            else:
                raise ValueError(f"bad char {ch!r} at row {r} col {c}")
        grid.append(row)
    return grid


def main():
    os.makedirs(OUT_DIR, exist_ok=True)

    # Remove any stale per-sprite JSONs from previous PixelLab runs so the
    # index stays in sync.
    for fn in os.listdir(OUT_DIR):
        if fn.endswith(".json"):
            os.remove(os.path.join(OUT_DIR, fn))

    index = []
    for s in SPRITES:
        ident = s["name"].lower().replace(" ", "_")
        frames = []
        for grid_str, hold in s["frames"]:
            frames.append({"hold": hold, "grid": parse_grid(grid_str)})
        doc = {
            "name": s["name"],
            "category": s["category"],
            "description": f"Hand-pixeled Wine-Edition sprite: {s['name']}",
            "palette": s["palette"],
            "frame_count": len(frames),
            "frames": frames,
        }
        out_path = os.path.join(OUT_DIR, ident + ".json")
        with open(out_path, "w") as f:
            json.dump(doc, f, indent=2)
        index.append({
            "filename": ident + ".html",  # convert_to_c.js strips .html
            "name": s["name"],
            "category": s["category"],
            "frame_count": len(frames),
            "palette_size": 10,
        })
        print(f"  wrote {out_path}  ({len(frames)} frame(s))")

    with open(os.path.join(OUT_DIR, "_index.json"), "w") as f:
        json.dump(index, f, indent=2)
    print(f"  wrote {OUT_DIR}/_index.json  ({len(index)} sprites)")


if __name__ == "__main__":
    main()
